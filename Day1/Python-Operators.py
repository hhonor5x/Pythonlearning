#Python Operators

#Arthimetic Operators
# +, -, *, %, //, /
print(10+2)
print(10-2)
print( 10*2 )
print( 10 % 3)
print(10/3)
print(10//3)

# Uniary operators
# uninary plus (+) and uninary minus (-)
print(+3)
print(-3)

# Bitwise operator --> Bitwise negation (~)
print(~3-4) # result is -8

# bitwise or (|), bitwsie and (&) and bitwise XOR (^)

print( 1  | 2 )
print ( 1 & 2 )
print(2^1)

# Exponential operator (**)
print( 2 ** 3) # result is 2 * 2 * 2

# Boolean operators
print ( 3 and 1) # result is 1  # always the 2 value will be the output

print ( 1 or 4 ) # result is 2 # always 1st value will be the output

print ( (2 or 3) and 3 )

# element of operator ( in )
print ( 1 in [2,2,1])

# comparison operators

print ( 1 > 2 )
print ( 1 < 2 )
print ( 1 == 2 )
print ( 1 <= 2 )
print ( 1 >=2 )
print ( 1 != 2 )


